public class TempletVMNode {

    public String vmName;
    public String vmFileName;
    public String outurl;
    public String exampleapi;
    public String exampleapifile;
    
    public String getVmName() {
        return vmName;
    }
    public void setVmName(String vmName) {
        this.vmName = vmName;
    }
    public String getVmFileName() {
        return vmFileName;
    }
    public void setVmFileName(String vmFileName) {
        this.vmFileName = vmFileName;
    }
    public String getOuturl() {
        return outurl;
    }
    public void setOuturl(String outurl) {
        this.outurl = outurl;
    }
    public String getExampleapi() {
        return exampleapi;
    }
    public void setExampleapi(String exampleapi) {
        this.exampleapi = exampleapi;
    }
    public String getExampleapifile() {
        return exampleapifile;
    }
    public void setExampleapifile(String exampleapifile) {
        this.exampleapifile = exampleapifile;
    }
     
    
}
